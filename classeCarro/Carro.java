package classeCarro;

public class Carro {
	String modelo, tipo, cor, quantPortas, acelerar, ligarFarol, desacelerar, darSeta;

	public String getModelo() {
		return modelo;
	}
	public void setModelo (String modelo) {
		this.modelo = modelo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo (String tipo) {
		this.tipo = tipo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public String getQuantPortas(){
		return quantPortas;
	}
	public void setQuantPortas (String quantPortas){
		this.quantPortas = quantPortas;
	}
	public String getAcelerar(){
		return acelerar;
	}
	public void setAcelerar (String acelerar){
		this.acelerar = acelerar;
	}
	public String getLigarFarol(){
		return ligarFarol;
	}
	public void setLigarFarol (String ligarFarol){
		this.ligarFarol = ligarFarol;
	}
	public String getDesacelerar(){
		return desacelerar;
	}
	public void setDesacelerar (String desacelerar){
		this.desacelerar = desacelerar;
	}
	public String getDarSeta(){
		return darSeta;
	}
	public void setDarSeta (String darSeta){
		this.darSeta = darSeta;
	}
}
