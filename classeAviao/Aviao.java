package classeAviao;

public class Aviao {
	String modelo, tipo, cor, quantLugares, decolar, aterrissar, pousar, abrirPorta;

	public String getModelo() {
		return modelo;
	}
	public void setModelo (String modelo) {
		this.modelo = modelo;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo (String tipo) {
		this.tipo = tipo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public String getQuantLugares(){
		return quantLugares;
	}
	public void setQuantLugares (String quantLugares){
		this.quantLugares = quantLugares;
	}
	public String getDecolar(){
		return decolar;
	}
	public void setDecolar (String decolar){
		this.decolar = decolar;
	}
	public void setAterrissar (String aterrissar){
		this.aterrissar = aterrissar;
	}
	public String getAterrissar(){
		return aterrissar;
	}
	public void setPousar (String pousar){
		this.pousar = pousar;
	}
	public String getPousar(){
		return pousar;
	}
	public void setAbrirPorta (String abrirPorta){
		this.abrirPorta = abrirPorta;
	}
	public String getAbrirPorta(){
		return abrirPorta;
	}

}

