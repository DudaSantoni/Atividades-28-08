package classeAnimal;

public class Animal {

	String tipo, porte, cor, numPatas, som, correr, comer, pular;

	public String getTipo() {
		return tipo;
	}
	public void setTipo (String tipo) {
		this.tipo = tipo;
	}
	public String getPorte() {
		return porte;
	}
	public void setPorte (String porte) {
		this.porte = porte;
	}
	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public String getNumPatas(){
		return numPatas;
	}
	public void setNumPatas (String numPatas){
		this.numPatas = numPatas;
	}
	public String getSom(){
		return som;
	}
	public void setSom (String som){
		this.som = som;
	}
	public String getCorrer(){
		return correr;
	}
	public void setCorrer (String correr){
		this.correr = correr;
	}
	public String getComer(){
		return comer;
	}
	public void setComer (String comer){
		this.comer = comer;
	}
	public String getPular(){
		return pular;
	}
	public void setPular (String pular){
		this.pular = pular;
	}
}

